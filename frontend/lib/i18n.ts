export function getMessage(language: Language, key: string, fallback?: string): string {
  return messages[language][key] ?? messages.en[key] ?? fallback ?? key
}

export function getCategoryLabel(language: Language, slug: string, fallback: string): string {
  return getMessage(language, `common.category.${slug}`, fallback)
}

export function getRoomTypeLabel(language: Language, roomType: string): string {
  const slug = roomType.toLowerCase().replace(/\s+/g, '-')
  return getMessage(language, `common.roomType.${slug}`, roomType)
}
